#negative indexing in lists means that the last element has index -1, the second last has index -2 and so on...

languages = ["Python", "Swift", "C++"]

# access item at index 0
print(languages[-1])   # C++

# access item at index 2
print(languages[-3])   # Python

#in this, the last element has index -1, the second last has index -2 and so on... so the output is C++ and Python and not Python and C++..

