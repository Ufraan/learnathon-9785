#items in a list can be changed and are mutable. we use the assignment operator (=) to change an item or a range of items

languages = ['Python', 'Swift', 'C++']

# changing the third item to 'C'
languages[2] = 'C'

print(languages)  # ['Python', 'Swift', 'C']

#here, initially the third item was C++ but we changed it to C using the assignment operator (=)

#so output is will be as follows:
# ['Python', 'Swift', 'C']