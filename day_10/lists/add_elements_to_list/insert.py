#insert method adds an element at a specified position in the list
#for example:

numbers = [10, 30, 40]

# insert an element at index 1 (second position)
numbers.insert(1, 20)

print(numbers) # [10, 20, 30, 40]


#in this, the element 20 is added at index 1 (second position) and the output is [10, 20, 30, 40]

#so output will be as follows:
#[10, 20, 30, 40]


