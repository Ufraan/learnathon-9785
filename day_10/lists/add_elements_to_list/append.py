#the append method adds an element to the end of the list
numbers = [21, 34, 54, 12]

print("Before Append:", numbers)

# using append method
numbers.append(32)

print("After Append:", numbers)

#output will be as follows:
# Before Append: [21, 34, 54, 12]
# After Append: [21, 34, 54, 12, 32]


